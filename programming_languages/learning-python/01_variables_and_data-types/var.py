# declaring variables:
num = 23
flo = 3.14
wrd = "hello, world"
idf = True

print(num, flo, wrd, idf)

# type casting
num = str(num)
print(num)

flo = float(num)
print(flo)

wrd = int("34")
print(wrd)

idf = bool(num)
print(idf)

# type
print(type(num))
print(type(flo))
print(type(wrd))
print(type(idf))

# None

num = None
print(num)
print(type(num))




